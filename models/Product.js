const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// Create Schema
const ProductScheme = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = Product = mongoose.model("product", ProductScheme);
